package Entidad;

/**
 *
 * @author Usuario
 */
public class Raices {
    
    public double a;
    public double b;
    public double c;
    
    public Raices() {
    }
    
    public Raices(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    @Override
    public String toString() {
        return "Raices{" + "a=" + a + ", b=" + b + ", c=" + c + '}';
    }
    
    
}
